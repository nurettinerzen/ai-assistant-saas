export function formatTl(amount, locale = 'tr') {
  if (amount === null || amount === undefined) return null;
  return new Intl.NumberFormat(locale === 'en' ? 'en-US' : 'tr-TR', {
    style: 'currency',
    currency: 'TRY',
    maximumFractionDigits: 0
  }).format(Number(amount));
}
